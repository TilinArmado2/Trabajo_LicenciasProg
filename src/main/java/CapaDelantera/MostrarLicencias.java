    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
 try {
        FachadaProfesores fachada = new FachadaProfesores();
        List<Profesores> lista = fachada.obtenerProfesores();

        DefaultTableModel modelo = (DefaultTableModel) tblMostrar.getModel();
        modelo.setRowCount(0);

        for (Profesores p : lista) {
            modelo.addRow(new Object[]{
                p.getCI(),
                p.getNombre(),
                p.getTurno(),
                p.getGrupos(),
                p.getFechaInicio(),
                p.getFechaFin()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al mostrar los datos: " + e.getMessage());
    }
       
    }//GEN-LAST:event_btnMostrarActionPerformed

